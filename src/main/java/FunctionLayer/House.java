package FunctionLayer;

public class House {

    private int lenght;
    private int width;
    private int height;

    public House(int lenght, int width, int height) {
        this.lenght = lenght;
        this.width = width;
        this.height = height;
    }

    public int getLenght() {
        return lenght;
    }

    public void setLenght(int lenght) {
        this.lenght = lenght;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}


